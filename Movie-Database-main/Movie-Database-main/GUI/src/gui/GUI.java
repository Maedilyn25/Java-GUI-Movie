    private void btnMovUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMovUpdateActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblMovie = (DefaultTableModel) jTable5.getModel();
        if (jTable5.getSelectedRowCount() == 1) {
            //if single row is selected then update

            String movieMovie = movMovie.getText();
            String year = movYear.getText();
            String genre = movGenre.getText();
            String length = movLength.getText();

            //set updated value on table row
            tblMovie.setValueAt(movieMovie, jTable5.getSelectedRow(), 1);
            tblMovie.setValueAt(year, jTable5.getSelectedRow(), 2);
            tblMovie.setValueAt(genre, jTable5.getSelectedRow(), 3);
            tblMovie.setValueAt(length, jTable5.getSelectedRow(), 4);

            //update message display
            JOptionPane.showMessageDialog(this, "Updated Successfully..");
            
            movMovie.setText("");
            movYear.setText("");
            movGenre.setText("");
            movLength.setText("");

        } else {
            if (jTable5.getRowCount() == 0) {

                //if table is empty
                JOptionPane.showMessageDialog(this, "Table is Empty..");
            } else {

                //if no row is selected or multiple row is selected
                JOptionPane.showMessageDialog(this, "Select single row to be updated..");
            }

        }
    }//GEN-LAST:event_btnMovUpdateActionPerformed

