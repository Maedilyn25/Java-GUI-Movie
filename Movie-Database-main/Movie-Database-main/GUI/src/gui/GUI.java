    private int idDirec=0;
    private void direcButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_direcButtonMouseClicked
        // TODO add your handling code here:
        // TODO add your handling code here:
        
        if (diName.getText().equals("") || diBirthdate.getText().equals("") || diNumDirected.getText().equals("")
                || diNumActor.getText().equals("")) {
            //if any of three field left blank then left message
            JOptionPane.showMessageDialog(this, "Please enter all Data");
        } else {
            
            idDirec++;    
            int ID = idDirec;
            
            // if all data field
            // store enter data into String array --data---
            String directorData[] = {Integer.toString(ID), diName.getText(), diBirthdate.getText(),
                 diNumDirected.getText(), diNumActor.getText()};

            DefaultTableModel tblDirector = (DefaultTableModel) jTable10.getModel();
            //add string array data
            tblDirector.addRow(directorData); //row added

            //successfully added message
            JOptionPane.showMessageDialog(this, "Add Data Successfully");
            //clear textfield for new entry
            diName.setText("");
            diBirthdate.setText("");
            diNumDirected.setText("");
            diNumActor.setText("");
        }
    }//GEN-LAST:event_direcButtonMouseClicked

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel tblHome = (DefaultTableModel) jTable1.getModel();

        //set data to text field when raw is selected
        String tblMovie = tblHome.getValueAt(jTable1.getSelectedRow(), 1).toString();
        String tblCompany = tblHome.getValueAt(jTable1.getSelectedRow(), 2).toString();
        String tblDirector = tblHome.getValueAt(jTable1.getSelectedRow(), 3).toString();

        //set to textfield
        txtMovie.setText(tblMovie);
        txtCompany.setText(tblCompany);
        txtDirector.setText(tblDirector);
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblDirector = (DefaultTableModel) jTable1.getModel();
        if (jTable1.getSelectedRowCount() == 1) {
            //if single row is selected then update

            String movie = txtMovie.getText();
            String company = txtCompany.getText();
            String director = txtDirector.getText();

            //set updated value on table row
            tblDirector.setValueAt(movie, jTable1.getSelectedRow(), 1);
            tblDirector.setValueAt(company, jTable1.getSelectedRow(), 2);
            tblDirector.setValueAt(director, jTable1.getSelectedRow(), 3);

            //update message display
            JOptionPane.showMessageDialog(this, "Updated Successfully..");
            
            txtMovie.setText("");
            txtCompany.setText("");
            txtDirector.setText("");

        } else {
            if (jTable1.getRowCount() == 0) {

                //if table is empty
                JOptionPane.showMessageDialog(this, "Table is Empty..");
            } else {

                //if no row is selected or multiple row is selected
                JOptionPane.showMessageDialog(this, "Select single row to be updated..");
            }

        }
    }//GEN-LAST:event_btnUpdateActionPerformed

